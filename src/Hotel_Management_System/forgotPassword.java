    private void jTextField3KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            String mail=jTextField3.getText();
            if(mail.equals("")){
                JOptionPane.showMessageDialog(this, "Every field is required");
            }
            else{
                try {
                pst=con.prepareStatement("SELECT * FROM user WHERE BINARY email='"+mail+"'");
                ResultSet rs=pst.executeQuery();
                if(rs.next()){
                    jTextField1.setText(rs.getString("question"));
                    jTextField1.setEditable(false);
                    jTextField3.setEditable(false);
                    jTextField2.requestFocus();
                }
                else{
                    JOptionPane.showMessageDialog(this, "email not found");
                }
            } catch (SQLException ex) {
                Logger.getLogger(forgotPassword.class.getName()).log(Level.SEVERE, null, ex);
                JFrame jf=new JFrame();
                jf.setAlwaysOnTop(true);
                
            }
            }
            
        }
    }//GEN-LAST:event_jTextField3KeyPressed

