    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String email=jTextField3.getText();
        String questi=jTextField1.getText();
        String answer=jTextField2.getText();
        String passs=jPasswordField2.getText();
        
        if(email.equals("")||questi.equals("")||answer.equals("")||passs.equals("")){
            JOptionPane.showMessageDialog(this, "Every field is required");
        }
        else{
            try {
                pst=con.prepareStatement("select * from user where email=? and question=? and answer=?");
                pst.setString(1, email);
                pst.setString(2, questi);
                pst.setString(3, answer);
                
                
                
                ResultSet rs=pst.executeQuery();
                if(rs.next()){
                    pst=con.prepareStatement("update user set password='"+passs+"' where email='"+email+"'");
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(this, "password set successfully");
                    dispose();
                    new forgotPassword().setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this, "incorrect answer");
                }
            } catch (SQLException ex) {
                Logger.getLogger(forgotPassword.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
               
    }//GEN-LAST:event_jButton2ActionPerformed

